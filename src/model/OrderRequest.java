package model;

import java.util.List;

public class OrderRequest {
	private String order_inf;
	private String order_name;
	private String st_name;
	private String price;
	private String order_num;
	private String cu_id;
	private String order_time;
	private String order_comments;
	private String state;
	private List<OrderModel> orderList;

	public List<OrderModel> getOrderList() {
		return orderList;
	}
	public void setOrderList(List<OrderModel> orderList) {
		this.orderList = orderList;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getOrder_inf() {
		return order_inf;
	}
	public void setOrder_inf(String order_inf) {
		this.order_inf = order_inf;
	}
	public String getOrder_name() {
		return order_name;
	}
	public void setOrder_name(String order_name) {
		this.order_name = order_name;
	}
	public String getSt_name() {
		return st_name;
	}
	public void setSt_name(String st_name) {
		this.st_name = st_name;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getOrder_num() {
		return order_num;
	}
	public void setOrder_num(String order_num) {
		this.order_num = order_num;
	}
	public String getCu_id() {
		return cu_id;
	}
	public void setCu_id(String cu_id) {
		this.cu_id = cu_id;
	}
	public String getOrder_time() {
		return order_time;
	}
	public void setOrder_time(String order_time) {
		this.order_time = order_time;
	}
	public String getOrder_comments() {
		return order_comments;
	}
	public void setOrder_comments(String order_comments) {
		this.order_comments = order_comments;
	}
}
