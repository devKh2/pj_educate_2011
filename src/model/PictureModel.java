package model;

public class PictureModel {
	private int seq;
	private String picpath;
	private String table_num;
	private String user_picname;
	private String server_picname;
		
	public PictureModel(String picpath, String table_num, String user_picname,
			String server_picname) {
		this.picpath = picpath;
		this.table_num = table_num;
		this.user_picname = user_picname;
		this.server_picname = server_picname;
	}
	public PictureModel(int seq, String picpath, String table_num,
			String user_picname, String server_picname) {
		super();
		this.seq = seq;
		this.picpath = picpath;
		this.table_num = table_num;
		this.user_picname = user_picname;
		this.server_picname = server_picname;
	}
	
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
	public String getPicpath() {
		return picpath;
	}
	public void setPicpath(String picpath) {
		this.picpath = picpath;
	}
	public String getTable_num() {
		return table_num;
	}
	public void setTable_num(String table_num) {
		this.table_num = table_num;
	}
	public String getUser_picname() {
		return user_picname;
	}
	public void setUser_picname(String user_picname) {
		this.user_picname = user_picname;
	}
	public String getServer_picname() {
		return server_picname;
	}
	public void setServer_picname(String server_picname) {
		this.server_picname = server_picname;
	}
}
