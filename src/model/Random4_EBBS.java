package model;

public class Random4_EBBS {
	private String ebbs_title;
	private String ebbs_score;
	private String ebbs_artid;
	private String st_name;
	private String st_num;
	private String picpath;
	
	public String getEbbs_artid() {
		return ebbs_artid;
	}
	public void setEbbs_artid(String ebbs_artid) {
		this.ebbs_artid = ebbs_artid;
	}
	
	public String getEbbs_title() {
		return ebbs_title;
	}
	public void setEbbs_title(String ebbs_title) {
		this.ebbs_title = ebbs_title;
	}

	
	public String getEbbs_score() {
		return ebbs_score;
	}
	public void setEbbs_score(String ebbs_score) {
		this.ebbs_score = ebbs_score;
	}
	public String getSt_num() {
		return st_num;
	}
	public void setSt_num(String st_num) {
		this.st_num = st_num;
	}
	public String getSt_name() {
		return st_name;
	}
	public void setSt_name(String st_name) {
		this.st_name = st_name;
	}
	public String getPicpath() {
		return picpath;
	}
	public void setPicpath(String picpath) {
		this.picpath = picpath;
	}
	

}
