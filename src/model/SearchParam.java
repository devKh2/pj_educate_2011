package model;

public class SearchParam {
	private int p;			//requestPageNumber
	private String sc;			//select (searchKey)
	private String kw; 			//keyWord (searchValue)
	private String detail; 			//detailPageName
	private String st_type;			//searchStoreSect
	private String menu_sect;		//searchMenuSect
	private String state;
	private int page_per_count;		//페이지당 표시되는 list 갯수
	private String sort_method;		//검색 된 detail 페이지 정렬 선택 변수 
	private int firstRow;
	private int endRow;

	
	public int getFirstRow() {
		return firstRow;
	}

	public void setFirstRow(int firstRow) {
		this.firstRow = firstRow;
	}

	public int getEndRow() {
		return endRow;
	}

	public void setEndRow(int endRow) {
		this.endRow = endRow;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}
	public String getSort_method() {
		return sort_method;
	}

	public void setSort_method(String sort_method) {
		this.sort_method = sort_method;
	}

	public int getPage_per_count() {
		return page_per_count;
	}

	public void setPage_per_count(int page_per_count) {
		this.page_per_count = page_per_count;
	}

	public SearchParam() {
		this.p = 1;
		this.page_per_count = 10;
		this.sort_method = "default";
	}
	
	public int getP() {
		return p;
	}
	public void setP(int p) {
		this.p = p;
	}
	public String getSc() {
		return sc;
	}
	public void setSc(String sc) {
		this.sc = sc;
	}

	public String getKw() {
		return kw;
	}

	public void setKw(String kw) {
		this.kw = kw;
	}

	public String getDetail() {
		return detail;
	}

	public void setDetail(String detail) {
		this.detail = detail;
	}

	
	public String getSt_type() {
		return st_type;
	}

	public void setSt_type(String st_type) {
		this.st_type = st_type;
	}

	public String getMenu_sect() {
		return menu_sect;
	}

	public void setMenu_sect(String menu_sect) {
		this.menu_sect = menu_sect;
	}


	
	
}
